package cards;

public class CardStats {
	
	private static int ship;
	private static int system;
	
	
	
	
	
	/* -_-_-_-_-_-_-_- GETTERS & SETTERS -_-_-_-_-_-_-_- */

	public int getShip() {
		return ship;
	}

	public static void setShip(int ship) {
		CardStats.ship = ship;
	}

	public int getSystem() {
		return system;
	}

	public static void setSystem(int system) {
		CardStats.system = system;
	}
	
}
